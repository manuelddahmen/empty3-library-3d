package one.empty3.feature.app.replace.java.awt;

public class Point {

    public double x;
    public double y;

    public Point(int x2, int y2) {
        this.x = x2;
        this.y = y2;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
